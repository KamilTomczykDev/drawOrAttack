class Card {
  constructor(
    name,
    defaultAttack,
    attack,
    defaultHealing,
    healing,
    cost,
    defaultTurns,
    turns,
    ability,
    img,
    id
  ) {
    this.name = name;
    this.defaultAttack = defaultAttack;
    this.attack = attack;
    this.defaultHealing = defaultHealing;
    this.healing = healing;
    this.cost = cost;
    this.defaultTurns = defaultTurns;
    this.turns = turns;
    this.ability = ability;
    this.img = img;
    this.id = id;
  }
}

